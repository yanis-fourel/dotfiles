-- Map j and k to gj/gk, but only when no count is given
-- However, for larger jumps like 6j add the current position to the jump list
local function vertical_move(motion)
  local BIG = 5

  return function()
    local count = vim.v.count or 1
    if count > BIG then
      vim.cmd "mark '"
    end
    if count == 0 then
      return 'g' .. motion
    end
    return motion
  end
end
